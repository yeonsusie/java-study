package ch06Exercise;

public class Printer {
	//인스턴스 메소드 선언
	public void println(int a){
		System.out.println(a);
		return;
	}

	public void println(boolean a){
		System.out.println(a);
		return;
	}
	
	public void println(double a){
		System.out.println(a);
		return;
	}
	
	public void println(String a){
		System.out.println(a);
		return;
	}
	
	
	//static 메소드 선언
	static void println1(int a){
		System.out.println(a);
		return;
	}

	static void println1(boolean a){
		System.out.println(a);
		return;
	}
	
	static void println1(double a){
		System.out.println(a);
		return;
	}
	
	static void println1(String a){
		System.out.println(a);
		return;
	}
}
